                        <div id="search_panel" class="search-panel" aria-hidden="true">
                            <input class="search-input" type="text" id="search_keyword" name="search_keyword"
                                value="" autocomplete="off" aria-label="Search Model or Keyword"
                                placeholder="Search" />
                            <button aria-label="Search" class="search-button submit-button" type="button"
                                id="search_box_submit_btn_nodejs">
                                <i class="icon-search" id="search_box_submit_icon" aria-hidden="true"></i>
                            </button>
                        </div>
                        <script type="text/javascript">
                            $(document).ready(function() {
                                $("#search_keyword").attr("tabindex", "-1")
                                $('#search_box_submit_btn_nodejs, #search_keyword, #toggle_search').on('click keyup', function(e) {
                                    e.stopPropagation();
                                    if (e.type == "keyup" && e.which != 13) { // Not Enter key
                                        return true;
                                    }

                                    if ($('#search_box_form #search_keyword').hasClass('focus')) {
                                        //collapse
                                        $("#search_keyword").attr("tabindex", "-1")
                                        if (($('#search_keyword').val()).trim() == '') {
                                            e.preventDefault();
                                            if ($(this).attr('id') == "search_box_submit_btn_nodejs") {
                                                collapse_search_field()
                                            }
                                        } else {
                                            if ($(this).attr('id') == "search_box_submit_btn_nodejs") {
                                                go_to_search_link();
                                            }
                                        }
                                    } else {
                                        //expand
                                        $('#search_panel').removeAttr("aria-hidden");
                                        $('#search_box_form').addClass("expanded")
                                        $('#search_box_form #search_keyword').addClass('focus');
                                        $('#search_box_form #toggle_search').attr("aria-expanded", "true");
                                        $('#search_box_form #toggle_search').attr("aria-hidden");
                                        $("#search_keyword").attr("tabindex", "0");
                                        $("#search_keyword").focus();
                                    }
                                });
                                $('#search_keyword').focusout(function() {
                                    collapse_search_field();
                                });
                            });

                            function collapse_search_field() {
                                if (($('#search_keyword').val()).trim() == '') {
                                    $('#search_box_form').removeClass("expanded");

                                    $('#search_box_form #search_keyword').removeClass('focus');
                                    $('#search_box_form #toggle_search').attr("aria-expanded", "false");
                                    $('#search_box_form #toggle_search').removeAttr("aria-hidden");
                                    $('#search_panel').attr("aria-hidden", "true");
                                }
                            }

                            function go_to_search_link() {
                                var searchString = encodeURIComponent(($('#search_box_form #search_keyword').val()).trim());
                                if (searchString != '' && searchString != 'Search') {
                                    var search_link = '/products/search/keyword/' + searchString;
                                    window.location.href = search_link;
                                }
                            }
                        </script>
